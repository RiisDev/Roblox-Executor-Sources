void decrypt(byte buf[16], byte key[32])
{
	add_round_key(buf, key);
	inv_shift_rows(buf);
	inv_sub_bytes(buf);

	byte rcon = 0x80;

	for (byte i = 1; i < 14; ++i)
	{
		if (i & 1)
		{
			expand_dec_key(key, rcon);
			add_round_key(buf, key + 16);
		}
		else
			add_round_key(buf, key);

		inv_mix_columns(buf);
		inv_shift_rows(buf);
		inv_sub_bytes(buf);
	}

	add_round_key(buf, key);
}

void cbc_inv(byte buf[16], byte key[32], byte IV[16])
{
	byte tmp[16];
	for (int i = 0; i < 16; i++)
		tmp[i] = buf[i];

	decrypt(buf, key);
	for (int i = 0; i < 16; i++)
		buf[i] ^= IV[i];
	for (int i = 0; i < 16; i++)
		IV[i] = tmp[i];
}

std::string AES256_Encrypt(std::string const & plaintext, std::string const & key)
{
	std::string result;
	std::string data = plaintext;

	// Create random IV
	byte IV[16];
	for (int i = 0; i < 16; i++)
	{
		IV[i] = rand() % 256;
		result += ((char)IV[i]);
	}

	// Add PKCS#7 padding
	unsigned int size = data.size();
	unsigned int pad_len = 16 - (size % 16);
	for (unsigned int i = 0; i < pad_len; i++) data += (char)pad_len;

	// Encrypt data
	for (size_t i = 0; i < size; i += 16)
	{
		// Copy encoding key
		byte enc_key[32];
		for (int o = 0; o < 32; o++)
			enc_key[o] = key[o];

		// Get next block
		byte buf[16];
		for (int o = 0; o < 16; o++)
			buf[o] = data[o + i];

		// Encrypt
		cbc(buf, enc_key, IV);

		// Append to result
		for (int o = 0; o < 16; o++)
			result += (char)buf[o];
	}

	return result;
}

std::string AES256_Decrypt(std::string const & data, std::string const & key)
{
	std::string result;
	unsigned int size = data.length();

	// Check data length
	if (size <= 16 || size % 16 != 0)
		return "Invalid data";

	// Get IV
	byte IV[16];
	for (int i = 0; i < 16; i++)
		IV[i] = data[i];

	// Generate decoding key
	byte rcon = 1;
	byte dec_key2[32];
	for (int i = 0; i < 32; i++)
		dec_key2[i] = key[i];
	while (rcon != 0x80)
		expand_enc_key(dec_key2, rcon);

	// Decrypt data
	for (size_t i = 16; i < size; i += 16)
	{
		// Copy decoding key
		byte dec_key[32];
		for (int o = 0; o < 32; o++)
			dec_key[o] = dec_key2[o];

		// Get block to decrypt
		byte buf[16];
		for (int o = 0; o < 16; o++)
			buf[o] = data[o + i];

		// Decrypt block
		cbc_inv(buf, dec_key, IV);

		// Add block to result
		for (int o = 0; o < 16; o++)
			result += (char)buf[o];
	}

	// Remove PKCS#7 padding
	int res_len = result.length();
	int pad_len = (int)result[res_len - 1];
	if (res_len - pad_len < 0)
		return "Invalid data";
	result.erase(res_len - pad_len, pad_len);

	return result;
}