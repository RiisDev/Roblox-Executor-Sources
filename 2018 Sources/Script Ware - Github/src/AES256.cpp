void expand_enc_key(byte key[32], byte &rcon)
{
	key[0] ^= s_sbox[key[29]] ^ rcon;
	key[1] ^= s_sbox[key[30]];
	key[2] ^= s_sbox[key[31]];
	key[3] ^= s_sbox[key[28]];

	rcon <<= 1;

	for (byte i = 4; i < 16; ++i)
		key[i] ^= key[i - 4];

	for (byte i = 16; i < 20; ++i)
		key[i] ^= s_sbox[key[i - 4]];

	for (byte i = 20; i < 32; ++i)
		key[i] ^= key[i - 4];
}

byte rj_xtime(byte x)
{
	return (x & 0x80) ? ((x << 1) ^ 0x1b) : (x << 1);
}

void mix_columns(byte buf[16])
{
	byte * b = buf;

	for (byte i = 0; i < 4; ++i)
	{
		byte tmp[4];
		std::copy(b, b + 4, tmp);

		byte m = b[0] ^ b[1] ^ b[2] ^ b[3];

		for (byte j = 0; j < 4; ++j)
			*b++ ^= m ^ rj_xtime(tmp[j] ^ tmp[(j + 1) & 3]);
	}
}

void shift_rows(byte buf[16])
{
	byte tmp;

	tmp = buf[1];
	buf[1] = buf[5];
	buf[5] = buf[9];
	buf[9] = buf[13];
	buf[13] = tmp;

	tmp = buf[10];
	buf[10] = buf[2];
	buf[2] = tmp;

	tmp = buf[3];
	buf[3] = buf[15];
	buf[15] = buf[11];
	buf[11] = buf[7];
	buf[7] = tmp;

	tmp = buf[14];
	buf[14] = buf[6];
	buf[6] = tmp;
}

void sub_bytes(byte buf[16])
{
	for (byte i = 0; i < 16; ++i)
		buf[i] = s_sbox[buf[i]];
}

void add_round_key(byte buf[16], const byte key[16])
{
	for (int i = 0; i < 16; i++)
		buf[i] ^= key[i];
}

void encrypt(byte buf[16], byte key[32])
{
	add_round_key(buf, key);
	byte rcon = 1;

	for (byte i = 1; i < 14; ++i)
	{
		sub_bytes(buf);
		shift_rows(buf);
		mix_columns(buf);

		if (i & 1)
		{
			add_round_key(buf, key + 16);
		}
		else
		{
			expand_enc_key(key, rcon);
			add_round_key(buf, key);
		}
	}

	sub_bytes(buf);
	shift_rows(buf);
	expand_enc_key(key, rcon);
	add_round_key(buf, key);
}

void cbc(byte buf[16], byte key[32], byte IV[16])
{
	for (int i = 0; i < 16; i++)
		buf[i] ^= IV[i];
	encrypt(buf, key);
	for (int i = 0; i < 16; i++)
		IV[i] = buf[i];
}

void inv_mix_columns(byte buf[16])
{
	byte * b = buf;

	for (byte i = 0; i < 4; ++i)
	{
		byte tmp[4];
		std::copy(b, b + 4, tmp);

		byte m1 = b[0] ^ b[1] ^ b[2] ^ b[3];
		byte m2 = rj_xtime(m1);

		byte m3[2];
		m3[0] = m1 ^ rj_xtime(rj_xtime(m2 ^ tmp[0] ^ tmp[2]));
		m3[1] = m1 ^ rj_xtime(rj_xtime(m2 ^ tmp[1] ^ tmp[3]));

		for (byte j = 0; j < 4; ++j)
			*b++ ^= m3[j & 1] ^ rj_xtime(tmp[j] ^ tmp[(j + 1) & 3]);
	}
}

void expand_dec_key(byte key[32], byte & rcon)
{
	for (byte i = 31; i >= 20; --i)
		key[i] ^= key[i - 4];

	for (byte i = 19; i >= 16; --i)
		key[i] ^= s_sbox[key[i - 4]];

	for (byte i = 15; i >= 4; --i)
		key[i] ^= key[i - 4];

	rcon >>= 1;

	key[0] ^= s_sbox[key[29]] ^ rcon;
	key[1] ^= s_sbox[key[30]];
	key[2] ^= s_sbox[key[31]];
	key[3] ^= s_sbox[key[28]];
}

void inv_sub_bytes(byte buf[16])
{
	for (byte i = 0; i < 16; ++i)
		buf[i] = s_inv_sbox[buf[i]];
}

