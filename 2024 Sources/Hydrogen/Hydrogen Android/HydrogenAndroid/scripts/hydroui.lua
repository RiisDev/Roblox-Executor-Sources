local function tween_new(instances, ...)
	if not instances then return end
	if type(instances) ~= "table" then instances = {instances} end
	
	local tweens = table.clone(instances)
	for idx,instance in pairs(tweens) do
		tweens[idx] = service"TweenService":Create(instance, ...)
	end
	
	local t,reflection = {},{}
	reflection.__index = function(_, k)
		if k == "__original" then return instances end
		for _,tween in pairs(tweens) do
			local v = tween[k]
			if type(v) == "function" then 
				local success,msg = pcall(v) -- if v succeeds, i'm sorry (can be fixed)
				if not msg then return v end
				local needle = "Expected ':' not '.'"
				if string.sub(msg, 1, #needle) == needle then
					return function(...)
						return v(tween, ...)
					end
				end
			end
			return v
		end
	end
	setmetatable(t, reflection)
	
	return t
end

local function in_range(pos:Vector2, obj:GuiObject)
	local obj_pos = obj.AbsolutePosition
	local obj_size = obj.AbsoluteSize

	return pos.X >= obj_pos.X and pos.X <= obj_pos.X + obj_size.X and pos.Y >= obj_pos.Y and pos.Y <= obj_pos.Y + obj_size.Y
end

local function make_draggable(target: GuiObject, from: GuiObject, cb)	
	if not from then from = target end
	if not cb then cb = function() return true end end
	
	local is_dragging,last_pos = false,nil
	service'UserInputService'.InputBegan:Connect(function(input: InputObject)		
		if ((input.UserInputType == Enum.UserInputType.MouseButton1) or (input.UserInputType == Enum.UserInputType.Touch)) and (in_range(Vector2.new(input.Position.X, input.Position.Y), from)) then
			is_dragging = true
			last_pos = nil
		end
	end)
	service'UserInputService'.InputChanged:Connect(function(input) 
		if not cb(is_dragging) then 
			is_dragging = false
			return 
		end
		if not is_dragging then return end
		
		if (input.UserInputType == Enum.UserInputType.MouseMovement) or (input.UserInputType == Enum.UserInputType.Touch) then		
			local v2 = Vector2.new(input.Position.X, input.Position.Y)
			if not last_pos then last_pos = v2 end
			local d = v2-last_pos
			last_pos = v2
			target.Position += UDim2.fromOffset(d.X, d.Y)
		end
	end)
	service'UserInputService'.InputEnded:Connect(function(input) 
		if (input.UserInputType == Enum.UserInputType.MouseButton1) or (input.UserInputType == Enum.UserInputType.Touch) then
			is_dragging = false
			last_pos = nil
		end
	end)
end

