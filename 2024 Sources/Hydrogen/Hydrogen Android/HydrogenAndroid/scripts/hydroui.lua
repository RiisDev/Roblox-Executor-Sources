		throw_notification(2, "You are already verified!", 5)
		return
	end
	
	local url = string.format("https://api.hydrogen.sh/gateway/auth?key=%s&hwid=%s", Converted["_VerifyTB"].Text, getgenv().getreg().TDXP4J2MQBH163PZT1S1())

	local status, res = pcall(http_get, url)

	if not status then
		if getgenv().getreg().PEOU2IGKB4VKSBGPYQADA then
			throw_notification(2, "Key expired!", 5)
			getgenv().getreg().TDXP4J2MQBH163PZT1S169()
			getgenv().getreg().PEOU2IGKB4VKSBGPYQADA = nil
		else
			throw_notification(2, "Invalid key!", 5)
		end

		return
	end

	if string.find(res, "expiresAt") then
		throw_notification(1, "Verified! Have fun!", 5)
		getgenv().getreg().TDXP4J2MQBH163PZT1S0(Converted["_VerifyTB"].Text)
	else
		if getgenv().getreg().PEOU2IGKB4VKSBGPYQADA then
			throw_notification(2, "Key expired!", 5)
		else
			throw_notification(2, "Invalid key!", 5)
		end
	end
end)

