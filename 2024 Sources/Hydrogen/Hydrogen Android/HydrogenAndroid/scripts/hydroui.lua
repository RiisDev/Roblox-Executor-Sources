service"RunService".RenderStepped:Connect(function()
	-- handle rainbow
	do
		local h,s,v = (os.clock()%10)/10,0.2,1
		local c = Color3.fromHSV(h,s,v)
		
		Converted["_AuthTutText"].TextColor3 = c
	end
	
	-- handle notification duration status
	do
		for _,notification:Frame in pairs(Converted["_HydrogenGui"]:GetChildren()) do
			if notification.Name == "Notification" then
				local s = notification:GetAttribute("start")
				if not s then continue end
				local d = notification:GetAttribute("duration")
				if not d then continue end
				local dbg:Frame = notification:FindFirstChild("DurationBg")
				if not dbg then continue end
				
				dbg.Size = UDim2.new((os.clock()-s)/d, 0, 1, 0)
			end
		end
	end
end)

